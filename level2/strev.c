char    *ft_strrev(char *str)
{
	int i = 0;
	int len = 0;
	int k = 0;
	int tmp = 0;
	
	while (str[k] != 0)
	{
		k++;
		len++;
	}
	while (i < len / 2)
	{
		tmp = str[i];
		str[i] = str[len - 1 - i];
		str[len - 1 -i] = tmp;
		i++;
	}
	return str;
}