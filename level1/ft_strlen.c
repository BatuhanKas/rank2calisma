#include <stdio.h>

int	ft_strlen(char *str)
{
	int i = 0;
	while (str[i])
	{
		i++;
	}
	return (i);
}

int	main()
{
	int x = 0;
	char s1[] = "batuhan";
	x = ft_strlen(s1);
	printf("%d", x);
}