#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int tmp;
	
	tmp = *a;
	*a = *b;
	*b = tmp;
}

int	main()
{
	int x = 5;
	int y = 10;

	int *px = &x;
	int *py = &y;

	printf ("%d, %d\n", *px, *py);
	ft_swap(px, py);
	printf ("%d, %d", *px, *py);
}